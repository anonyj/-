function [P N] =getnurbs(Q,R,M,k)

P0=Q(1,:);
P1=Q(2,:);
P2=Q(3,:);
P3=Q(4,:);
for i=1:length(k(1,:))-1
 N=getn(k(1,i),M(1,1),M(1,2),M(1,3),M(1,4),M(1,5),M(1,6),M(1,7),M(1,8));
 P(i,:)=(N(1,1)*R(1,1)*P0+N(1,2)*R(1,2)*P1+N(1,3)*R(1,3)*P2+N(1,4)*R(1,4)*P3)/(N(1,1)*R(1,1)+N(1,2)*R(1,2)+N(1,3)*R(1,3)+N(1,4)*R(1,4));
end
end