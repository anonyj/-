     N(1,1)=0;
     N(1,2)=(u5-x)^3/((u5-u2)*(u5-u3)*(u5-u4));
     N(1,3)=((x-u2)*(u5-x)^2)/((u5-u2)*(u5-u3)*(u5-u4))+((x-u3)*(u5-x)*(u6-x))/((u5-u3)*(u5-u4)*(u6-u3))+((x-u4)*(u6-x)^2)/((u6-u3)*(u6-u4)*(u5-u4));
