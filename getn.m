elseif  (u5<=x)&&(x<u6)
     N(1,1)=0;
     N(1,2)=0;
     N(1,3)=(u6-x)^3/((u6-u3)*(u6-u4)*(u6-u5));
     N(1,4)=((x-u3)*(u6-x)^2)/((u6-u3)*(u6-u4)*(u6-u5))+((x-u4)*(u6-x)*(u7-x))/((u6-u4)*(u6-u5)*(u7-u4))+((x-u5)*(u7-x)^2)/((u7-u4)*(u7-u5)*(u6-u5)); 
